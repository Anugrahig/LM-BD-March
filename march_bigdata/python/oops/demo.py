# OOPS concept - Object oriented programming

# 3 Terms
# 1. Class
# 2. Object
# 3. Reference

# Class : Base model or blueprint
# Object : Realworld Entity
# Reference : Operation performed on Object

# eg:
# TV : samsung,sony,redmi,mi,realme,oneplus

# Class : TV
# Object : samsung,sony,redmi,mi,realme,oneplus

# Reference : Volume Up,down



# Mobile : jio,samsung,poco,redmi,mi,realme,oneplus

# Class : Mobile
# Object : jio,samsung,poco,redmi,mi,realme,oneplus

# Reference : Calling,Photo,Gaming




