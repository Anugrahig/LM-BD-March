# Rules for creating a variable
# 1) starting space (blank space) is not allowed
# eg:  num=20 #error
# num=20

# 2) Space not supported in b/w
# eg:  company name="Luminar"  #error
# company_name="Luminar"

# 3) First letter must be alphabets or underscore(_)
# eg:  1num=40 #error
# num1=40
# num_1=40

# 4) reserved keywords not allowed for variable name
# reserved keyword : if,else,for,while,break,continue,pass,elif
# eg:  if=40 #error

# 5) Variables are case sensitive
# num=10 Num=20 NUM=30 # 3 different variable

# 6) Always use noun (Meaningful name)
# a=10 b=20 c=30 instead num1=10 num2=20 num3=30





