# Relational Operator - Used for Comparison
# <,>,<=,>=,==,!=
# Output will be True or False
num1=10
num2=20

# True ===> 1

# False ===> 0

# True< False #1<0 False
# True> False #1>0 True
# True >= True # 1>=1 True
# True <= True # 1><=1 True

print(num1<num2)

print(num1>num2)

print(num1>=num2)

print(num1<=num2)

print(num1==num2)

print(num1!=num2)




